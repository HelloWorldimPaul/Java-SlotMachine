import java.util.Random;
import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);


        int balance;
        int bet;
        int payout;
        String playMore;
        String[] row;


        System.out.println("*************************");
        System.out.println("Welcome to Scatter");
        System.out.println("*************************");
        System.out.print("Deposit Amount: ");
        balance = scanner.nextInt();

        while (balance > 0 ) {
            System.out.println("Current balance: $" + balance);
            System.out.print("Place your bet amount: ");
            bet = scanner.nextInt();

            if (bet > balance) {
                System.out.println("INSUFFICIENT FUNDS");

            } else if (bet <= 0) {
                System.out.println("Bet must be greater than 0");

            } else {
                balance -= bet;

            }
            System.out.println("SPINNING");
            row = spinRow();
            printRow(row);
            payout = getPayout(row, bet);

            if (payout > 0) {
                System.out.println("You won $" + payout);
                balance += payout;
            } else {
                System.out.println("You lost huuhuhuhu");
            }
            do {
                System.out.println("Do you want to play more? (y/n): ");
                playMore = scanner.next().toLowerCase();

                if (!playMore.equals("y") && !playMore.equals("n")) {
                    System.out.println("Invalid input");
                }

            } while(!playMore.equals("y") && !playMore.equals("n"));

            if (playMore.equals("n")) {
                System.out.print("Goodbye loser");
                break;
            }

        }
        scanner.close();
    }

    static String[] spinRow() {

        String[] symbols = {"üçí", "üçâ", "üçã", "üîî", "‚≠ê"};
        String[] row = new String[3];
        Random random = new Random();


        for (int i = 0; i < 3; i++) {
            row[i] = symbols[random.nextInt(symbols.length)];
        }

        return row;

    }

    static void printRow(String[] row) {
        System.out.println("**************");
        System.out.println(String.join(" | ", row));
        System.out.println("**************");
    }

    static int getPayout(String[] row, int bet) {

        if (row[0].equals(row[1]) && row[1].equals(row[2])) {
            return switch (row[0]) {
                case "üçí" -> bet * 3;
                case "üçâ" -> bet * 4;
                case "üçã" -> bet * 4;
                case "üîî" -> bet * 4;
                case "‚≠ê" -> bet * 4;
                default -> 0;
            };
        } else if (row[0].equals(row[1])) {
            return switch (row[0]) {
                case "üçí" -> bet * 2;
                case "üçâ" -> bet * 3;
                case "üçã" -> bet * 4;
                case "üîî" -> bet * 5;
                case "‚≠ê" -> bet * 10;
                default -> 0;
            };
        } else if (row[1].equals(row[2])) {
            return switch (row[1]) {
                case "üçí" -> bet * 2;
                case "üçâ" -> bet * 3;
                case "üçã" -> bet * 4;
                case "üîî" -> bet * 5;
                case "‚≠ê" -> bet * 10;
                default -> 0;
            };
        }
        return 0;

    }
}



